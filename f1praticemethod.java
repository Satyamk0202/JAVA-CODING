package f;
// write a java method to print multiplication table of a number n
public class f1praticemethod {
static void multiplication(int n){
    for(int i=1;i<=10;i++){
        System.out.println(n+"*"+i+"="+n*i);
        
    }
  
}
    public static void main(String[] args) {
      multiplication(5);
        
    }
}
